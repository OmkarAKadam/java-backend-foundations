public class Variables {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = a + b;
        System.out.println("Int Result: " + c);

        float f = 10.5f;
        double d = 20.5;
        double result = f + d;
        System.out.println("Double Result: " + result);

        String name = "Kaizenki - Omkar Kadam";
        System.out.println("String Result: " + name);
    }
}
